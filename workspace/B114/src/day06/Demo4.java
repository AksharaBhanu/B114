package day06;

public class Demo4 {

	public static void main(String[] args) {
		int a=10;
		a=a+1;
		a+=1;
		a++;//it will increment value of a by 1
		System.out.println(a);//13
		
		a--;//it will decrement value of a by 1
		System.out.println(a);//12
		
		a=a-1;
		a-=1;
		System.out.println(a);//10

	}

}
