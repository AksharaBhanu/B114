package day06;

public class Demo7 {

	public static void main(String[] args) {
		int a;
		a=10;
		System.out.println(a++);//10
		System.out.println(a);//11
		
		
		a=10;
		System.out.println(++a);//11
		System.out.println(a);//11
		
		a=10;
		System.out.println(a--);//10
		System.out.println(a);//9
		
		
		a=10;
		System.out.println(--a);//9
		System.out.println(a);//9
		
		
	}

}
